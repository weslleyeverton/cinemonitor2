class Filme {

    private String titulo;
    private int ano;
    private int tipo;

    public void setTitulo(String titulo){
      this.titulo = titulo;
    }
    public void setAno(int ano){
      this.ano = ano;
    }
    public void setTipo(int tipo){
      this.tipo = tipo;
    }

    public String getTitulo(){
      return titulo;
    }

    public int getAno(){
      return ano;
    }

    public int getTipo(){
      return tipo;
    }

}